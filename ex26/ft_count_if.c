int	ft_count_if(char **tab, int (*f)(char*))
{
	int i;
	int kol;

	i = 0;
	kol = 0;
	while (tab[i] != 0)
	{
		if ((*f)(tab[i]) == 1)
			kol++;
		i++;
	}
	return (kol);
}
